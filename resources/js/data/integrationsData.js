export function integrationToggleState(integration, enabled) {
    if (integration.id === 'tiktok' && enabled) {
        return {
            enabled: true,
            statusColor: 'var(--warn)',
            statusLabel: 'Rate-limited',
        };
    }

    if (enabled) {
        return {
            enabled: true,
            statusColor: '#4ADE80',
            statusLabel: 'Active',
        };
    }

    return {
        enabled: false,
        statusColor: 'rgba(20,17,15,.2)',
        statusLabel: 'Disabled',
    };
}
